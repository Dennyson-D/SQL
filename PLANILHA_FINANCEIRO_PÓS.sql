 ,E.PARCELA,H.IDLAN,A.IDPERLET,E.VALOR,K.CODBOLSA,E.IDPARCELA,A.CODFILIAL,D.RA,D.CODCONTRATO,L.MESDECOMPETENCIA,M.VALORBAIXA,M.VALOR,N.VALORBAIXA,E.VALOR,L.VALOROP2
