SELECT  DENSE_RANK()OVER(/*PARTITION BY IND*/ ORDER BY IND ASC)-1 AS QTD_ACORDOS,AA.* FROM
(
	SELECT DISTINCT B.IDACORDO,A.IDLAN,A.NUMERODOCUMENTO,B.CLASSIFICACAO CLASSACORDO,A.CLASSIFICACAO CLASFLAN,A.STATUSLAN,A.MESDECOMPETENCIA,A.VALORORIGINAL,A.VALORBAIXADO,A.CODCFO,
	CASE WHEN A.CLASSIFICACAO = 0 THEN '0' 
	ELSE CASE WHEN A.CLASSIFICACAO = 15 THEN CONVERT(INT,ISNULL(SUBSTRING(numerodocumento,1,ISNULL(CHARINDEX('/',numerodocumento),1)-1),'0')) END
	END IND,
	B.RECCREATEDON
	
	FROM FLAN A
	LEFT JOIN FACORDOREL B ON (B.IDLAN=A.IDLAN)
	JOIN FACORDO C ON (C.IDACORDO=B.IDACORDO)
	WHERE A.IDLAN IN  (1455049,
	1455052,
	1467812,
	1467813,
	1477429,
	1477430,
	1487878,
	1498121,
	1508355,
	1508356,
	1518522,
	1522320,
	1541003,1541004,1541005,1541006,1541007,1541008,1541009)  --(1532189,1478709)  -- CLASSIFICAÇÃO 0,15  HISTORICO

	--C.IDACORDO=39525
)AA




