,CONVERT(MONEY,SUM(VALORORIGINAL)) AS VALORORIGINAL
,CONVERT(MONEY,SUM(VALORBAIXA)) AS VALORBAIXA

 FROM FLANBAIXA BB WHERE IDLAN = 932503 AND DATABAIXA < = @DATA_CORTE --AND DATACANCELBAIXA IS NULL

 GROUP BY IDLAN,DATACANCELBAIXA

