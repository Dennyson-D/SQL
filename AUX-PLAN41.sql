, (SELECT MAX(DATABAIXA) FROM FLANBAIXA WHERE FLANBAIXA.IDLAN = BB.IDLAN AND CONVERT(VARCHAR(10),FLANBAIXA.DATABAIXA,103) < = @DATA_CORTE AND DATACANCELBAIXA IS NULL ) AS DATABAIXA1
